TextStyle heebo(double fontSize, Color? color, FontWeight weight,
    {TextDecoration? decoration}) {
  return GoogleFonts.heebo(
      fontSize: fontSize.sp,
      fontWeight: weight,
      color: color,
      decoration: decoration);
}

